package Aula04;

public class Matematica0103 
{

public int adicao(int numero01, int numero02)
{
	return numero01+numero02;
}

public float adicao(float numero01, float numero02)
{
	return numero01+numero02;
}

public int subtracao(int numero01, int numero02)
{
	return numero01-numero02;
}

public float subtracao(float numero01, float numero02)
{
	return numero01-numero02;
}

public int multiplicacao(int numero01, int numero02)
{
	return numero01*numero02;
}

public float multiplicacao(float numero01, float numero02)
{
	return numero01*numero02;
}

public float divisao(int numero01, int numero02)
{
	return numero01/numero02;
}

public double divisao(float numero01, float numero02)
{
	return numero01/numero02;

}
}